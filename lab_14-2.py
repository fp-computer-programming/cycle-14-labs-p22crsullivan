# Import turtle
import turtle

# Create the window
window = turtle.Screen()
window.setup(500, 500)
window.title("Lab 2")
joseph = turtle.Turtle()
# Direct the turtle
joseph.forward(100)
joseph.right(120)
joseph.forward(100)
joseph.right(120)
joseph.forward(100)
# Make sure the window doesn't close
window.mainloop()